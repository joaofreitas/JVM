class objeto{
	private int ab;
	private double cb;
	
	public objeto(int ab, double cb){
		this.ab = ab;
		this.cb = cb;
	}
	
	public void set_a(int ab){
		this.ab = ab;
	}
	public void set_c(double c){
		this.cb = cb;
	}
	public int get_a(){
		return ab;
	}
	public double get_c(){
		return cb;
	}
}
